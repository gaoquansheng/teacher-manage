package com.entity;

public class emergencycontact {
	private int emergency_id;
	private String emergency_name;
	private String emergency_address;
	private String emergency_tel;
	private String user_idno;
	public int getEmergency_id() {
		return emergency_id;
	}
	public void setEmergency_id(int emergency_id) {
		this.emergency_id = emergency_id;
	}
	public String getEmergency_name() {
		return emergency_name;
	}
	public void setEmergency_name(String emergency_name) {
		this.emergency_name = emergency_name;
	}
	public String getEmergency_address() {
		return emergency_address;
	}
	public void setEmergency_address(String emergency_address) {
		this.emergency_address = emergency_address;
	}
	public String getEmergency_tel() {
		return emergency_tel;
	}
	public void setEmergency_tel(String emergency_tel) {
		this.emergency_tel = emergency_tel;
	}
	public String getUser_idno() {
		return user_idno;
	}
	public void setUser_idno(String user_idno) {
		this.user_idno = user_idno;
	}

}
